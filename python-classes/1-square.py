#!/usr/bin/python3

class Square:
    """
    A class representing a square with a given size.

    Attributes:
        size (int): The size of the square.
    """

    def __init__(self, size):
        """
        Initializes a Square object with the given size.

        Args:
            size (int): The size of the square.
        """
        self.size = size
